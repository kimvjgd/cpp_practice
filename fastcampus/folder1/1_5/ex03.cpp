#include <iostream>

using namespace std;

int main()
{
  int num0 = 11;   // 10진수
  int num1 = 011;  // 8진수
  int num2 = 0b11; // 2진수
  int num3 = 0x11; // 16진수

  cout << "num 0 " << num0 << endl;
  cout << "num 1 " << num1 << endl;
  cout << "num 2 " << num2 << endl;
  cout << "num 3 " << num3 << endl;
}